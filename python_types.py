

# checking the type of a variable
width = 15
print(type(width))  # Output: <class 'int'>

name = "Alice"
print(type(name))  # Output: <class 'str'>

length = 20.5
print(type(length))  # Output: <class 'float'>

# checking the type of a function
print(type(print))  # Output: <class 'builtin_function_or_method'>